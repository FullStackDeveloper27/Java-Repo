package Chapter_05_Operators;

public class LogicalOperators {
    public static void main(String[] args) {
        System.out.println("Logical Operator");
        boolean a = true;
        boolean b = false;
        System.out.println("a && b " + (a && b));
        System.out.println("a || b " + (a || b));
        System.out.println("!b " + !(b));
        System.out.println("!a " + !(a));
    }
}
