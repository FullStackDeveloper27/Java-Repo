package Chapter_05_Operators;

public class RelationalOperator {
    public static void main(String[] args) {
        System.out.println("Relational Operator");
        int a = 10;
        int b = 20;

        System.out.println("a == b " + (a == b));
        System.out.println("a > b " + (a > b));
        System.out.println("a < b " + (a < b));
        System.out.println("a >= b " + (a >= b));
        System.out.println("a <= b " + (a <= b));
        System.out.println("a != b " + (a != b));
    }
}
