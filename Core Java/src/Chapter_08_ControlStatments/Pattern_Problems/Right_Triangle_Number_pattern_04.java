package Chapter_08_ControlStatments.Pattern_Problems;

public class Right_Triangle_Number_pattern_04 {
    public static void main(String[] args) {
        for(int i=1;i<=5;i++){
            for (int j=1;j<=i;j++){
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
